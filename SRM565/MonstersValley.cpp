  int solve(int idx, long long sum)
  {
    if (idx >= (int)dread.size())
      return 0;

    int ret = solve(idx+1, sum+dread[idx]) + price[idx];
    if (sum >= dread[idx])
      ret = min(ret, solve(idx+1, sum));

    return ret;
  }
  

  int minimumPrice(vector<long long> dread, vector <int> price)
  {
    this->dread = dread;
    this->price = price;

    int ans = price[0];
    vector<vector<long long> > dp(dread.size(), vector<long long>(1000, 0LL));
    dp[0][price[0]] = dread[0];
    for (unsigned int i = 1; i < dp.size(); ++i) {
      for (unsigned int j = ans; j < dp[i].size(); ++j) {
        dp[i][j] = max(dp[i][j], dp[i-1][j]);
        if (j+price[i] < dp[i].size())
          dp[i][j+price[i]] = max(dp[i][j+price[i]], dp[i-1][j] + dread[i]);
      }
      for (int j = ans; j < (int)dp[i].size(); ++j) {
        if (dp[i][j] >= dread[i]) {
          ans = j;
          break;
        }
      }
    }
    return ans;
  }
  
